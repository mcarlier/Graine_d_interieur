            <p>Un kokedama c’est trois choses : une plante, de la mousse et du substrat pour lier le tout. Bien sûr il y a mille et une recettes pour mener à bien le projet, c’est surtout des plantes que l’on va mettre dedans que tout démarre.</p>

            <div class="corpsArticle">
              <h3>Comment choisir sa plante</h3>

              <p>Donc pour réaliser votre premier kokedama facilement il faut choisir des plantes résistantes et peu exigeantes. Il y a des fougères pas mal pour ça et surtout le lierre. les deux ensemble peuvent vous permettre de faire un joli petit kokedama en un rien de temps. Préparer la mousse : Maintenant que les plantes sont choisies passons à la mousse. Les plantes choisies aiment l’humidité alors le choix de la mousse est facile, il suffit de prendre une mousse bien épaisse.</p>

              <img src="./images/articles/kokedama1.jpg" alt="kokedamas flottants">
              <p class="legende">Kokédamas flottants pour un effet léger</p>

              <p>Pour faire votre mélange il vous faudra de l’argile (celle pour faire de la poterie est très bien), du terreau (tout fera l’affaire, tourbe brune…) et de la sphaigne (au rayon des orchidées dans les jardineries). Mettez une boulette d’argile à tremper avec de l’eau pour obtenir une pâte, ajoutez y le terreau et la sphaigne hachée. </p>

              <img src="./images/articles/Kokedama4.jpg" alt="arrangement de kokedamas">
              <p class="legende">Arrangement de kokédama sur meuble en bois</p>
            </div>

            <h3>L'important c'est la mousse !</h3>
            <p>Vous avez tout ce qu’il vous faut, préparez votre plante en la dépotant, grattez sa terre pour mettre les racines à l’air. Enserrez les racines dans la boule de substrat. Faites une boule plus petite que la dimension que vous voudrez ensuite car il y a l’épaisseur de la mousse en plus. Mettez votre mousse autour et ficelez le tout avec un petit fil noir. Arrosez, voilà le tour est joué.</p>

